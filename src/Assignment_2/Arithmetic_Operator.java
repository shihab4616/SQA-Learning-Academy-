package Assignment_2;

public class Arithmetic_Operator {
    public static void main(String[] args) {
        int a=10,  b=20,  c;
        c=a+b;
        System.out.println(c); //30
        c=b-c;
        System.out.println(c);// -10
        c=a*b;
        System.out.println(c);//200
        c=a/b;
        System.out.println(c);//0
        c=a%b;
        System.out.println(c);//10
    }
}
