package Assignment_2;

public class Relational_Operator {

    public static void main(String[] args) {
        int a=30, b=50;
        System.out.println(a==b);//Fales
        System.out.println(a>b);//False
        System.out.println(a<b);//True
        System.out.println(a!=b);//True
        System.out.println(a>=b);//False
        System.out.println(a<=b);//True
    }
}
